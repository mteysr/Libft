#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	int		i;
	char	a;

	i = 0;
	a = 10;
	while (s[i])
	{
		write (fd, &s[i], 1);
		i++;
	}
	write(fd, &a, 1);
}
